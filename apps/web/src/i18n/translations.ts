export const TRANSLATIONS: Record<Locale, Dict> = { en, ta, hi, ml, kn };

export function translate(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>
): string {
  let text = TRANSLATIONS[locale][key] ?? TRANSLATIONS.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}

export function routeTypeKey(type: string): string {
  const map: Record<string, string> = {
    safest: "routes.safest",
    cheapest: "routes.cheapest",
    balanced: "routes.balanced",
    women_friendly: "routes.womenFriendly",
  };
  return map[type] ?? "routes.balanced";
}
